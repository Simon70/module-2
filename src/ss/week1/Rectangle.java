package ss.week1;


public class Rectangle {
	private int length;
	private int width;
	private int area;
	private int perimeter;
	
	public Rectangle(int length, int width) {
		this.length = length;
		this.width = width;
	}

	
	public int getLength() {
		return length;
	}

	
	public int getWidth() {
		return width;
	}

	
	public int getArea() {
		return area;
	}

	
	public int getPerimeter() {
		return perimeter;
	}
	
	
}
